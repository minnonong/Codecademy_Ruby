#  09_14 The Combined Comparison Operator

=begin
  복합 비교 연산자( Combined Comparison Operator )
    - <=>라고 씀.
    - 첫번째 피연산자 > 두번째 피연산자 : 1 반환.
    - 첫번째 피연산자 = 두번째 피연산자 : 0 반환.
    - 첫번째 피연산자 < 두번째 피연산자 : -1 반환.
=end
book_1 = "A Wrinkle in Time"

book_2 = "A Brief History of Time"

book_1 <=> book_2
